<?php

declare(strict_types=1);

/**
 * phpMyAdmin local launcher.
 *
 *   composer pma            -> install (if missing) and serve on :8081
 *   composer pma -- install -> install/upgrade only
 *   composer pma -- port=NNNN  override port
 *
 * Downloads the all-languages stable build from phpmyadmin.net into
 * tools/pma/ (gitignored), generates config.inc.php from .env so it
 * connects to the same MySQL the app uses, then runs the built-in PHP
 * dev server.
 */

const ROOT = __DIR__ . '/..';
const PMA_DIR = ROOT . '/tools/pma';
const PMA_VERSION = '5.2.1';
const PMA_URL = 'https://files.phpmyadmin.net/phpMyAdmin/' . PMA_VERSION . '/phpMyAdmin-' . PMA_VERSION . '-all-languages.zip';

require_once ROOT . '/vendor/autoload.php';

function out(string $msg): void { fwrite(STDOUT, $msg . PHP_EOL); }
function err(string $msg): void { fwrite(STDERR, $msg . PHP_EOL); }

function loadEnv(): array
{
    $path = ROOT . '/.env';
    if (!file_exists($path)) {
        throw new RuntimeException('.env not found. Run `composer setup` first.');
    }
    return parse_ini_file($path) ?: [];
}

function isInstalled(): bool
{
    return file_exists(PMA_DIR . '/index.php');
}

function downloadPma(): void
{
    if (!is_dir(ROOT . '/tools')) {
        mkdir(ROOT . '/tools', 0755, true);
    }

    $zipPath = ROOT . '/tools/pma.zip';
    out('Downloading phpMyAdmin ' . PMA_VERSION . ' ...');

    $ctx = stream_context_create(['http' => ['timeout' => 120, 'user_agent' => 'aureo-pma-installer']]);
    $bytes = @file_get_contents(PMA_URL, false, $ctx);
    if ($bytes === false) {
        throw new RuntimeException('Download failed from ' . PMA_URL);
    }
    file_put_contents($zipPath, $bytes);
    out('  Downloaded ' . number_format(strlen($bytes) / 1048576, 1) . ' MB.');

    out('Extracting ...');
    extractZip($zipPath, ROOT . '/tools');
    unlink($zipPath);

    $extracted = ROOT . '/tools/phpMyAdmin-' . PMA_VERSION . '-all-languages';
    if (!is_dir($extracted)) {
        throw new RuntimeException('Expected directory not found after extraction: ' . $extracted);
    }

    if (is_dir(PMA_DIR)) {
        // Replace prior install
        rrmdir(PMA_DIR);
    }
    rename($extracted, PMA_DIR);
    out('  Installed to ' . PMA_DIR);
}

function extractZip(string $zipPath, string $destDir): void
{
    if (class_exists(ZipArchive::class)) {
        $zip = new ZipArchive();
        if ($zip->open($zipPath) !== true) {
            throw new RuntimeException('Failed to open ' . $zipPath);
        }
        $zip->extractTo($destDir);
        $zip->close();
        return;
    }

    // Fallback for when ext-zip is disabled.
    if (stripos(PHP_OS, 'WIN') === 0) {
        // Prefer tar.exe (ships with Windows 10+); fall back to PowerShell Expand-Archive.
        $cmd = sprintf('tar -xf %s -C %s 2>&1', escapeshellarg($zipPath), escapeshellarg($destDir));
        exec($cmd, $output, $code);
        if ($code === 0) return;

        $ps = sprintf(
            'powershell -NoProfile -Command "Expand-Archive -Path %s -DestinationPath %s -Force"',
            escapeshellarg($zipPath),
            escapeshellarg($destDir)
        );
        exec($ps, $output, $code);
        if ($code === 0) return;

        throw new RuntimeException('Extraction failed via tar and PowerShell. Output: ' . implode("\n", $output));
    }

    $cmd = sprintf('unzip -q %s -d %s 2>&1', escapeshellarg($zipPath), escapeshellarg($destDir));
    exec($cmd, $output, $code);
    if ($code !== 0) {
        throw new RuntimeException('unzip failed (enable ext-zip or install unzip). Output: ' . implode("\n", $output));
    }
}

function rrmdir(string $dir): void
{
    if (!is_dir($dir)) return;
    $items = scandir($dir);
    foreach ($items as $item) {
        if ($item === '.' || $item === '..') continue;
        $path = $dir . DIRECTORY_SEPARATOR . $item;
        is_dir($path) && !is_link($path) ? rrmdir($path) : unlink($path);
    }
    rmdir($dir);
}

function writePmaConfig(array $env): void
{
    $rawHost = (string) ($env['DB_HOST'] ?? '127.0.0.1');
    $port = '3306';
    if (str_contains($rawHost, ':')) {
        [$rawHost, $port] = explode(':', $rawHost, 2);
    }
    $host = $rawHost ?: '127.0.0.1';
    $dbname = (string) ($env['DB_NAME'] ?? '');

    $secret = bin2hex(random_bytes(16));

    $config = <<<PHP
<?php
// Auto-generated by bin/pma.php — do not edit by hand.
declare(strict_types=1);

\$cfg['blowfish_secret'] = '{$secret}';

\$i = 0;
\$i++;
\$cfg['Servers'][\$i]['auth_type']   = 'cookie';
\$cfg['Servers'][\$i]['host']        = '{$host}';
\$cfg['Servers'][\$i]['port']        = '{$port}';
\$cfg['Servers'][\$i]['compress']    = false;
\$cfg['Servers'][\$i]['AllowNoPassword'] = true;
\$cfg['Servers'][\$i]['only_db']     = ['{$dbname}'];

\$cfg['UploadDir']      = '';
\$cfg['SaveDir']        = '';
\$cfg['ShowPhpInfo']    = false;
\$cfg['CheckConfigurationPermissions'] = false;
PHP;

    file_put_contents(PMA_DIR . '/config.inc.php', $config);
}

function parseArgs(array $argv): array
{
    $opts = ['install_only' => false, 'port' => 8081];
    foreach (array_slice($argv, 1) as $arg) {
        if ($arg === 'install') { $opts['install_only'] = true; continue; }
        if (str_starts_with($arg, 'port=')) { $opts['port'] = (int) substr($arg, 5); continue; }
    }
    return $opts;
}

// --- main ---------------------------------------------------------------

try {
    $opts = parseArgs($argv);
    $env = loadEnv();

    if (!isInstalled()) {
        downloadPma();
    } else {
        out('phpMyAdmin already installed at ' . PMA_DIR);
    }

    out('Writing config.inc.php from .env ...');
    writePmaConfig($env);

    if ($opts['install_only']) {
        out('Done. Run `composer pma` to launch.');
        exit(0);
    }

    $port = $opts['port'];
    out('');
    out("Starting phpMyAdmin on http://localhost:{$port}");
    out('Login with the same MySQL credentials configured in .env (Ctrl+C to stop)');
    out('');

    // Replace current process with the dev server so Ctrl+C is clean.
    $cmd = sprintf('php -S localhost:%d -t %s', $port, escapeshellarg(PMA_DIR));
    passthru($cmd, $code);
    exit($code);
} catch (Throwable $e) {
    err('phpMyAdmin launcher failed: ' . $e->getMessage());
    exit(1);
}
